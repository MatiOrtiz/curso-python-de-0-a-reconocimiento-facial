A={1,2,3,4}
B={2,3,5,6}
C={3,4,6,7}

#Equivalencia de Conjuntos
print("Los conjuntos son equivalentes:")
print(A==B)
print(A==C)
print(B==C)

#Unión de Conjuntos
print("Las uniones de conjunto son: ")
print(A|B)
print(A|C)
print(B|C)

#Intersección de Conjuntos
print("Las intersecciones de conjunto son: ")
print(A&B)
print(A&C)
print(B&C)

#Diferencia simétrica de Conjuntos
print("Las diferencias simétricas de conjunto son: ")
print(A^B)
print(A^C)
print(B^C)
